package forLoops;

public class NestedForLoops {

	public static void main(String[] args) {
		for( int j = 1; j <=50; j++) {
			
		     for( int i = 1; i<= 5; i++) {
			    System.out.print(i +" ");
	    	}
		     System.out.println();
		}

	}

}
